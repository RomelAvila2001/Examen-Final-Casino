    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean resultado = false;
        int codigo=Integer.parseInt(txtCodigo.getText());
        String nombre= txtNombre.getText();
        int saldo=Integer.parseInt(txtSaldo.getText());


        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Llene todos los campos solicitados");
        }else{
            if(controladorJugador.buscar(codigo)!= null){
                Jugador jugador = controladorJugador.buscar(codigo);
                jugador.setNombre(nombre);
                jugador.setSaldo(saldo);
                resultado =controladorJugador.actualizar(jugador);
            }else{
                Jugador jugador = new Jugador(nombre);
                resultado =controladorJugador.crear(jugador);
            }
            
            JOptionPane.showMessageDialog(this, "Operación : " + resultado);
            //cargarDatos();
            limpiar();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int eliminar = JOptionPane.showConfirmDialog(this, "¿Seguro quieres eliminar este Alumno?");
        int codigo=Integer.parseInt(txtCodigo.getText());
        
        Jugador jugador= controladorJugador.buscar(codigo);
        if(eliminar == JOptionPane.YES_OPTION){
            boolean resultado = controladorJugador.eliminar(jugador);
            JOptionPane.showMessageDialog(this, "Operación : " + resultado);
            limpiar();
            //cargarDatos();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        int fila = tblDatos.getSelectedRow();
        int cod = (int) tblDatos.getValueAt(fila, 0);
        String nombre = (String) tblDatos.getValueAt(fila, 1);
        int saldo = (int) tblDatos.getValueAt(fila, 2);

        txtCodigo.setText(String.valueOf(cod));
        txtNombre.setText(nombre);
        txtSaldo.setText(String.valueOf(saldo));
    }//GEN-LAST:event_tblDatosMouseClicked

