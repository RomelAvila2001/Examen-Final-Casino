    public void cargarDatos() {
       DefaultTableModel modeloTabla = (DefaultTableModel) tblDatos.getModel();

        modeloTabla.setRowCount(0);
        for (Jugador jugador : controladorJugador.findAll()) {
            Object[] rowData = {jugador.getId(),jugador.getNombre(),jugador.getSaldo(),jugador.getVictorias(),jugador.getDerrotas()};
            modeloTabla.addRow(rowData);
        }
        tblDatos.setModel(modeloTabla);
    }
